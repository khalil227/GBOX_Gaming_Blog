               <img src="../photo/accessories.png" alt="">
               <p>Microsoft has shown us what the future of gaming looks like with the next-gen Xbox Series X and Xbox Series S consoles. <br>
               
               But if you’re an Xbox One owner, don’t worry. Just because there are new consoles on the scene doesn’t mean your older
               one will get less love. Microsoft has confirmed that any licensed accessories you buy for Xbox One will also work with
               Series S and Series X. <br>
               
               That means even if you don’t want to upgrade just yet you won’t be wasting money on accessories, like one of our
               favorite Xbox One accessories, the brilliant-but-pricey Wireless Elite Controller Series 2. <br>
               
               But which accessories should you buy for your console? It’s easy to get carried away and buy everything when you only
               need a few key things. Aside from first-party and third-party pads, consider one of the best Xbox One headsets, an
               external hard drive or a subscription to one of Microsoft's fab gaming services.</p> 
               <br>
                

               <h3>Xbox One Elite Wireless Controller Series 2 </h3>
               <h5>Meet the pricey, pro-level controller for Xbox One and Windows 10</h5>

               <img src="../photo/accesories2.png" alt="">
               <p><a
                     href="https://www.amazon.com/dp/B003ZSP0WW?tag=georiot-us-default-20&th=1&psc=1&ascsubtag=trd-row-7726081787870106000-20&geniuslink=true">
                     <span>available in Amazon.</span></a></p> <br> <br>
               <p>The Xbox One controller is already a pretty solid piece of hardware. It improved on the Xbox 360's controller, which
               became the industry standard way-back-when. Somehow, Microsoft managed even further improvement in the form of the Xbox
               One Elite Wireless Controller.
               
               There are swappable thumbsticks and directional pads that offer a better feel than the standard Xbox One controller, as
               well as quick-hit triggers to help out in the most high-impact shooters. While it's pretty pricy for a pad at almost
               three times the cost of a standard controller, you definitely get what you pay for with this premium piece of hardware.</p> <br>
               <br>

               <h3>Turtle Beach Elite Pro </h3>
               <h5>Built like a tank with sound to match</h5>
               <img src="../photo/accessories3.png" alt="">
               <p><a
                     href="https://www.microsoft.com/en-us/d/turtle-beach-elite-pro-optic-limited-edition-gaming-headset/8pkshp0flhp6?ocid=AID2000142_aff_7593_1243925&tduid=(ir__nbsaqcgs20kfqizgkk0sohz3wu2xuplpg9ovzn6u00)(7593)(1243925)(kXQk6.ivFEQ-e9plz7ixGrucILzJ8ZYLQw)()&ranMID=24542&ranEAID=kXQk6*ivFEQ&ranSiteID=kXQk6.ivFEQ-e9plz7ixGrucILzJ8ZYLQw&epi=kXQk6.ivFEQ-e9plz7ixGrucILzJ8ZYLQw&irgwc=1&irclickid=_nbsaqcgs20kfqizgkk0sohz3wu2xuplpg9ovzn6u00&activetab=pivot%3aoverviewtab">
                     <span>available in Miscrosoft.</span></a></p> <br>
               <P>Turtle Beach is the longest established specialist gaming headset manufacturer, with a fanatical following among
               pro-gamers – and when you unbox its top-of-the-range Elite Pro, you can see why. It simply oozes no-expense-spared
               design, and sports all manner of neat touches born from decades of pro-gaming experience.
               
               But more importantly than that, it sounds spectacular, with huge bass and crystal-clear treble adding up to a sound
               which will allow you to get deeply immersed into whatever game you're playing. Comfort-wise, it's exemplary, with big,
               thick earpads that eliminate all ambient noise, and can be easily adjusted to fit all head-sizes. A neat feature lets
               you add spacing to the earpads to accommodate a pair of glasses.
               
               On an Xbox One, we'd recommend teaming it up with piece of kit called the Tactical Audio Adapter, which clips into the
               Xbox One controller and operates as an amplifier, adding some of the extra sound-control features which come in a
               separate graphic equaliser-style box called the Tactical Audio Controller (which is pricey but adds Dolby 7.1 Surround
               Sound).</P> <br>

               <h3>Xbox One Media Remote </h3>
               <h5>The best (cheapest) way to make your Xbox One feel like a grown-up streamer</h5>
               <img src="../photo/accessories4.png" alt="">
               <p><a
