                     <span>available in Amazon.</span></a></p> <br>
               <p>
                  Ever since the PlayStation 2 put a DVD player in the homes of millions, one constant has remained this millennium; using
                  a controller to watch videos on consoles is the worst. If you're using your Xbox One as a streamer, Blu-ray player, and
                  TV watcher, the Xbox One Media Remote is a great way to watch all of your favorite content without fumbling to remember
                  the proper controller command to bring up closed captions or time remaining.
                  
                  This remote is perfectly sized to fit every important command (including volume options that controllers and Xbox One
                  Smartglass cant handle), is very sleek, and has a backlight that's perfect for home theaters and late-night
                  video-watching.
