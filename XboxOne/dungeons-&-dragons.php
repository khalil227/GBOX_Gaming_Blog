               <h3>DARK ALLIANCE</h3> 
               <h1>Dungeons & Dragons: Dark Alliance Release Date</h1> 
               <h4>GBOX reveals exlusive details of the D&D project</h4>
               <h5>By Jon Ryan 31 Mar 2021 6:42 pm</h5>
            </div>
         </section>
         <section>
            <div class="article"> 
               <br>
               <p>Wizards of the Coast has announced that Dungeons & Dragons: Dark Alliance will be released on PS5, PS4, Xbox Series X/S,Xbox One, and PC on June 22, 2021.Alongside the release date, IGN is proud to share an exclusive dev diary from Tuque Games that reveals more details about Dark Alliance and how the project came to be.</p>

            <iframe width="560" height="315" src="https://www.youtube.com/embed/BaobG3z18zg" title="YouTube video player"
               frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture"
               allowfullscreen></iframe>
             

               <P>Jeffrey Hattem, Tuque Games' studio head and creative director, explained how the team began making their dream Dungeons
               & Dragons game before Wizards of the Coast even agreed to let them officialy do so.
               
               "We were like, why not approach Wizards of the Coast to see if we could do a Dungeons & Dragons game," Hattem said. "The
               response I got back from WotC was… they just wanted to shut us down basically. They didn’t want us to make a Dungeons &
               Dragons game, but we wanted to make one. So, we pivoted. We’re not going to work on a pitch, we’re going to make this
               game as if we’re going to make this game and let’s just go. YOLO.”
               
               At the start, the game shared a lot of similarities with Diablo, including it being a top-down, action RPG. However, it
               quickly evolved to something much more grand.</P>

               <img src="../photo/D&D102.png" alt="">

               <p>The team continued to build their dream D&D game, and the industry began to take notice. The team received six different
               offers from six different publishers to make the game a reality, but Wizards of the Coast ended up becoming the
               publisher for the game after it saw its potential. It even doubled the budget and tasked Tuque Games to "go bigger."
               
               Dungeons & Dragons: Dark Alliance will feature real-time combat and dynamic co-op gameplay, and will pit the "the
               infamous Drizzt Do’Urden and his legendary companions – Catti-brie, Bruenor, and Wulfgar – against some of the most
               iconic monsters from the world of Dungeons & Dragons. Frost Giants, Beholders, and White Dragons roam the frozen tundra
               of Icewind Dale in search of the Crystal Shard— and it’s up to the companions to muster the strength to stop them from
               claiming and using it to destroy their home."</p> <br>

