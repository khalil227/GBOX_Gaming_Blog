            <br>
            <div class="article">
               
               <p>The Xbox One, Microsoft's current-generation console, has seen a user interface upgrade and the addition of limited
                  backward compatibility, and it's still at the top of its game.</p> <br>
                  <h3>XBOX ONE : DESIGN</h3>
             
                  <iframe width="560" height="315" src="https://www.youtube.com/embed/Spz5olq7K1A" title="YouTube video player"
                     frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture"
                     allowfullscreen></iframe>
               
               <p>The Xbox One doesn't take many design cues from the Xbox 360 or the Xbox 360 Slim. Instead, it follows
               the philosophy of the original Xbox: giant black box. It's big, black, and rectangular, measuring 3 by 12.9 by 11.7
               inches (HWD) and weighing about seven pounds. It combines glossy and matte black finishes to lend some style, but no
               shine can get past its plain blockiness. The PlayStation 4 looks much nicer and slimmer, with its parallelogram shape
               and the ability to stand on its side to show off its multicolored status light (with an optional stand). The Xbox One
               has to be laid down horizontally, and is simply black with a white light on it.</p> <br>
               <p>The front is dominated by a matte black left half that holds the slot-loading Blu-ray drive, and a glossy right half
               that features a glowing, flat, touch-sensitive Xbox button. The button turns the system on if you don't want to use the
               Kinect or a controller, and it's just as infuriatingly sensitive as the Xbox 360 Slim's power and eject buttons.
               Brushing anything against it, even lightly, can trigger the button. Fortunately, given the voice controls, you don't
               have to actually touch the Xbox One or go anywhere near it unless you're changing a disc or setting up a gamepad. A USB
               port sits on the left side of the system, next to a pairing button for registering controllers. The back panel holds an
               HDMI input and output, the Kinect port, an Ethernet port, an optical audio port, two USB ports, and the power port.</p> <br>

               <h3>XBOX ONE : CONTROLLER </h3>

               <img src="../photo/review2.png" alt="">
               <p>The standard Xbox One controller is comfortable, but it's not the impressive leap in design that the PlayStation 4's
               DualShock 4 gamepad is. It's a mostly matte black gamepad that looks and feels almost identical to the Xbox 360's
               controller. Instead of the Start and Back buttons, the gamepad has Menu and View buttons that serve the same functions.
               The triggers provide individual force feedback, rumbling in response to what you're playing apart from the gamepad
               itself. This feels particularly good for racing games like Forza Motorsport 5, because the right trigger's resistance
               and response to acceleration feels much more precise with force feedback.
               
               The Xbox Elite Wireless Controller included in the Elite bundle is significantly sturdier, more comfortable, and more
               customizable than the standard controller. Outside of the bundle, it retails for $149.99 as an optional accessory.</p> <br>
               
               <p>Like its nearest and dearest rival, the Xbox One is absolutely loaded with potential. While it may
                  have struggled in
                  both user confidence and PR status in its first 12 months, Microsoft has done a confident job in
                  cutting away the dead
                  flesh of previous errors.

                  A shadow of the console it unveiled at E3 2013, the Xbox One is still better for it. It's now faster,
                  more powerful and
                  boasts enough base features to make even Sony look over its shoulder. PS4 may be selling more units,
                  but the Xbox One is
                  snapping at its heels with every passing month.</p> <br>
                  <br>
                  <h3>XBOX ONE : PREFORMANCE </h3> 
                  <P>The 1TB version of the Xbox One has a hybrid drive that uses solid state media for part of the on-board storage.
                  Microsoft claims this makes the system quicker to load than the hard-drive-only 500GB model. Killer Instinct was a hair
                  faster on the 1TB Xbox One, loading matches in 11-12 seconds compared with between 12-13 seconds on the 500GB system,
                  which is hardly a noticeable difference. Gears of War: Ultimate Edition was even less apparent, with both systems
                  getting to the game's title screen from the main menu in approximately 30 seconds. The 1TB Xbox One's hybrid drive might
                  be a little bit faster, but don't expect a big jump in performance.</P> <br>
                  <h3>XBOX ONE : Verdict </h3>
                  <p>
                     If you want a pure game system, the Xbox One and the PlayStation 4 are in a dead heat. As a media streamer, though,
                     especially if you subscribe to cable or satellite service, the Xbox One is a better bet. The soft abandonment of Kinect
                     isn't a big deal, especially with the new Windows 10-based interface and refinements to the menus. The console isn't
                     reliant on voice control to a significant extent any longer, and the addition of backward compatibility makes it more
                     attractive than ever. Ultimately, though, it's all about the games you want to play. Media features should come after
                     that when choosing a game console.
                  </p> <br>
