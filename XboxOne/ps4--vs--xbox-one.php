               <h1>IS BUYING A PS4 OR OBOX ONR STILL WORTH IT?</h1>
               <h4>The PS4 and Xbox One are in high demand, but they're past their prime</h4>
               <h5>By Marshall Honorof 10 January 2021 - tom's guide</h5> <br> <br>
            </div>
         </section>
         <section>
            <div class="article">

               <img src="../photo/Rectangle 29.png" alt="">
               
               <p>If you're in the market for a PS4 or Xbox One, you are surprisingly not alone. In spite of the PS5 and Xbox Series X
               coming out months ago, Sony and Microsoft's last-gen consoles are just as popular as ever, not to mention increasingly
               hard to find.
               </p> <br>
               <P>It's not difficult to understand why people are buying PS4s and Xbox Ones in droves. They can't find PS5s or Xbox Series
               Xs, and it's better to have an outdated console than no console at all — particularly if you needed something to give as
               a holiday gift.</P><br> <br> <br>

               <h3>WHY YOU SHOULF BUY PS4 OR XBOX ONE</h3>
                  <br>
               <p>During big sales events such as Black Friday and Cyber Monday, the Tom's Guide staff uses search trends to help us find
               deals. Imagine my surprise when I found that "PS4" and "Xbox One" were, far and away, two of the most popular search
               terms.</p> <br>
               <P>Customers were desperate to find PS4s and Xbox Ones (especially PS4s), and when we were lucky enough to find the elusive
               consoles in stock, they sold out almost as fast as their newer counterparts. (The Switch was also ridiculously popular
               this holiday season, but you can now find one pretty much anywhere. The PS4 and Xbox One are still fairly elusive.)
               
               As I said, I think the time to buy one of these consoles is past unless you can find a new-in-box model at a staggering
               discount. (Think $200 or less.) But clearly, there must be some reason why people are still buying these systems in
               droves.</P> <br>
               <p>The simple answer is that no one can find the PS5 or the Xbox Series X, whereas the PS4 and Xbox One are comparatively
               more available. Since we're very early in the next-gen consoles' life cycles, the current-gen consoles still play 99% of
               the same games. Combine this with the holiday compulsion to give gifts and it's not hard to understand why the PS4 and
               Xbox One are having one last hurrah.</p> <br> <br>
               <p>Still, I can't think of a great reason to buy a PS4 or Xbox One at this stage of the game, and it has to do with
               backwards compatibility. That, and the fact that the PS4 is in the process of being discontinued as I write this article</p>   <br>
               <img src="../photo/review2.png" alt=""> <br> <br>

               <h3>WHY YOU SHOULD NOT BUY PS4 OR XBOX ONE </h3> <br>
               <p>This is common knowledge for dedicated gamers at this point, but may not be immediately apparent to more casual
               consumers: The PS5 is fully backwards compatible with the PS4. The Xbox Series X is fully backwards compatible with the
               Xbox One, and partially backwards compatible with the Xbox 360 and original Xbox.
               
               As such, you can pay $300 for a PS4 or an Xbox One, or $500 for a PS5 or Xbox Series X. When you consider that you'll
               probably have to upgrade within a year or two anyway, putting that $300 toward a new console instead of an old one makes
               a lot of sense.</p> <br>
                  <img src="../photo/Rectangle 20.png" alt="">
               <p>Essentially, buying a last-gen console simply delays your next-gen purchase, while tacking an extra $300 onto the price
               tag. Except for immediate availability, there's no real benefit — and plenty of drawbacks, once you have to figure out
               what to do with a bulky, outdated, unsellable console a few years down the line.
               
               As for when you'll be able to pick up that shiny new PS5 or Xbox Series X, well, that's harder to answer.</p> <br>
            </div>
