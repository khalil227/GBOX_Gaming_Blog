            <p>The Xbox One abandoned almost all original 'features' and slowly refocused itself on the one thing it should have
               always
               been about: the games.
            
               While Microsoft has definitely lost the battle of the exclusives in this generation, it's already preparing for the
               next
               with a newly bolstered line-up of in-house studios. So even if you're investing in an Xbox One now, you're getting a
               console that's slowly been re-evolved into a proper gaming system.</p> <br>
            
            <h3>XBOX ONE REVIEW: SETUP AND FEATURES</h3>
            
            <img src="../photo/review1.png" alt="">
            
            <p>If all of this sounds intimidating, don't worry, setting this beast up is a doddle. Kinect, the power cable and – if
               you
               fancy watching live television through the Xbox One - most set-top boxes all slot neatly into the back. Once you boot
               it
               up, the console will ask you if you already have a profile. If you do, you'll need to go online to load it up. If not
               –
               or if online is an anathema to you – you can create a new one from scratch.
            
               Online is easier, though. If you have a profile, you simply login with your password and instantly, your
               Achievements,
               save games, friend-lists and avatars are all ported across. It doesn't wipe your Xbox 360 gamertag – rather you now
               have
               gamertags on two platforms.</p> <br>
            
            <h3>XBOX ONE REVIEW: CONTROLLER </h3>
            
            <img src="../photo/review2.png" alt="">
            <p>The triggers feel smooter, the D-pad and shoulder buttons feel more responsive and the twin-sticks meld snugly to the
               player's thumbs. The pad overall feels more molded to the player's hands and the smooth surface is svelte to the
               touch.
               Instead of a Start and Select button, you have an Apps and Menu button, but they serve the same purposes in-game.
            
               The Xbox One Elite Wireless controller, designed for pro gamers and the most hardcore of hardcore users, rounds off
               the
               Xbox One peripheral roster. It's reassuringly heavier than the standard version and comes with a stack of extra
               analog
               sticks and D-pad faces to customise your pad. There are even a pair of steel paddles that fit onto the back to give
               you
               even more controls. It's expensive - retailing in the £129.99 range - but it's a must for pros who want to build a
               pad
               scaled around their competitive play style.</p> <br>

               <img src="../photo/review3.png" alt="">
               <p>Like its nearest and dearest rival, the Xbox One is absolutely loaded with potential. While it may have struggled in
               both user confidence and PR status in its first 12 months, Microsoft has done a confident job in cutting away the dead
               flesh of previous errors.
               
               A shadow of the console it unveiled at E3 2013, the Xbox One is still better for it. It's now faster, more powerful and
               boasts enough base features to make even Sony look over its shoulder. PS4 may be selling more units, but the Xbox One is
               snapping at its heels with every passing month.</p> <br>
