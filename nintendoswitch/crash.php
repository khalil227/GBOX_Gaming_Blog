  There are moments in Crash Bandicoot 4: It's About Time where you will come to a crossroads. Not the literal kind, of which there are actually many, the levels forking and pickling your brain on more than one occasion<br>, but the spiritual. The kind that defines you, that reveals your exhausted, thousand-death weathered heart and shows you who you really are. Crash 4 is a game that forces you to look deep, deep down into your soul and ask yourself: just how bad do I want that one extra crate?<br>
  
  Crash 4 is hard, basically. Bastard hard. Toys For Bob, the former Skylanders studio that's since turned its hand to the recent Crash and Spyro trilogy remakes, and leads the way here, clearly sees that original trilogy's infamous difficulty as the template. Crash 4 is a direct sequel to Crash Bandicoot 3: Warped, Naughty Dog's last proper entry. All the Crash platformers since then - including another one directed by Mark Cerny! - seem to have been quietly redacted, as Activision goes big on the jortsy, VHS-fuzzed nostalgia of the mid-'90s PlayStation.
  
  Whether or not you think outrageous difficulty, of the window-smashing, gripping-a-controller-until-its-plastic-audibly-creaks variety is what makes or breaks a Crash game will likely define your experience with Crash 4. Playing through it with artificial speed certainly exacerbates things, but there are moments where it moves from being challenging to simply awkward, the challenge derived from intentional clumsiness baked into one of the game's many new mechanics. At times the frustration can drown out everything the game offers, but it is only at times, and what it offers is really an awful lot. There are two main ways to play Crash 4: 'modern' mode, and what Toys For Bob has called - rather cruelly, to anyone conscious of how distant the '90s now are - 'retro' mode. Retro is the Crash you'll remember: you collect Wumpa fruit to gain lives, and when you run out of lives you go back to the last save, Mario-style, while Gems, which now unlock skins, are earned in the traditional way. Modern mode is what the game recommends (and as such the one I played on for the most part), which is more carrot than retro mode's stick: Gems are still your overall objective, but are instead earned by collecting a certain percentage of a level's total Wumpa fruit, smashing all of its crates, completing a level with three deaths or less, and finding a hidden one somewhere along the way. There are no lives, so when you die you simply go back to the last checkpoint indefinitely.
