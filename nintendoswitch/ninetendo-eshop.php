The main sale features savings on some of the very best indie games available on Switch, with the likes of Hades, Carrion, Ori and the Will of the
Wisps, and Katana Zero, all getting price cuts.
Some games are so good they feature twice. Hades, for example, is also listed in the Supergiant sale alongside its excellent predecessors, Transistor and Bastion.
