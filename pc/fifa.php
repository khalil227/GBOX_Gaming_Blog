<div class="ArticleTitle1"> FIFA 21 is coming to EA Play and Xbox Game Pass for PC next week

</div>
  <div class="ArtclTit1">The latest addition to EA's long-running soccer series will soon be free for subscribers. 

  </div>
<img src="../photo/fifa.png" class="img1A">
<div class="ArticleCon">FIFA 21, the latest addition to EA's long-running soccer series, is headed to EA Play and Xbox Game Pass Ultimate next week, and that means that Xbox Game Pass for PC subscribers are going to get it too.

  EA Play members will also have access to exclusive FIFA 21 content including in-game challenges and rewards, FIFA Ultimate Team kits, Volta Football gear, and a seasonal XP boost at the start of each FUT season. Through the month of May, subscribers will also be able to snag a Volta Squad Boost featuring Groundbreaker Trent Alexander-Arnold and the Liverpool FC Apparel Set, plus 3,500 Volta coins.<br><br>

  It's a big addition for EA Play subscribers—the FIFA series is tremendously popular, although perhaps more in Europe than North America—and also potentially very lucrative for EA itself: Electronic Arts said in its most recent financial report that its strong quarterly results were "driven by live services outperformance in Ultimate Team and Apex Legends."<br><br>
<br>
Making the base game free will almost certainly expand the FUT audience dramatically, although it could invite fresh headaches for the publisher, too: A recent CBC report on leaked documents showing that EA wants to "funnel" FIFA players into FUT sparked considerable controversy, despite not revealing anything particularly new.<br>





EA Play's basic tier was added to Xbox Game Pass for PC in March, at no extra cost, making it a pretty fantastic deal for gamers who aren't hung up on the concept of ownership. You can get the full list of games included with Xbox Game Pass for PC and EA Play here, and details on how it works with Xbox Game pass in this FAQ.






