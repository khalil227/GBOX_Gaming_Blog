    Jungle Accessibility<br><br>

    One of our longer term goals is to increase position preference for the jungle in lower MMR. We’re attacking the problem from a few angles that we think will in particular reduce some of the esoteric optimizations and help onboard newer players to the role. These are the changes we're initially planning at time of writing, Tuesday the 27th; tunings may change by the time this goes up on Friday but the general rationale will still hold true:<br>

    Players can expect long-range engagements, wide-open spaces, and a colorful environment at the holiday destination. The development team behind the FPS explained some of its objectives with the new map.<br><br>

    “One of the main goals we had with Breeze was building larger and more open spaces with longer sightlines than the other maps in the pool,” said Sal Garozzo, senior game designer at Riot Games. “This provides an opportunity for different weapons and agents to shine. Each map should provide different strengths and weaknesses for the agents in the roster to explore while supporting different types of strategies and team compositions.”<br><br>
<br>
More forgiving jungle clears - With the current shorter jungle timers the pace of farming and ganking has gone up considerably, making every misstep a major setback for both the jungler and their team.<br>


Jungle Monster Base Attack Damage reduced across many camps<br>
Jungle Monster Attack Damage scaling reduced across many camps<br>
Camp Respawn Timer :: 2 minutes >>> 2 minutes 15 seconds<br>
Camp "bright' warning timer :: 15 seconds >>> 10 seconds<br>
Jungle Gold from non-buff Camps :: Increased by approximately 5%<br>


Reduced snowball - Another factor that is both causing too much stress but also too much influence over the game outcome is how hard the jungle position can snowball. We think reducing this will make the position easier to break into and more enjoyable long term.<br>

Reintroducing comeback XP - Large monsters grant 50 XP per level you are below the average level of the game minus 1 (if the average level is 7, you get comeback XP at level 5 and below)<br>
Smite Damage to Monsters :: 390-1000 (based on levels) >>> 500 at base, 1000 after Smite quest completion<br>

”<br>

Camp Leashing Clarity (future work) - A particularly important skill for high level jungle optimizations is to perfectly manage the monster leash ranges and drag them into optimal positions for max clear speed. While not removing this skill, we want to help newer junglers learn the rules and leash ranges of each monster camp.

