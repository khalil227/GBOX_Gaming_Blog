  <div class="ArtclTit1">Titanfall 2' will be free to play until May 3 

  </div>
<img src="../photo/titanfall.jpg" class="img1A">
<div class="ArticleCon"><br>
    Respawn Entertainment’s mech-based FPS Titanfall 2 is free to play this weekend on Steam.<br>

    Capitalising on the game’s resurge in popularity, Respawn Entertainment  have made the game available for free this weekend.<br><br>

    The free trial began yesterday (April 30) at 10am PST (5pm GMT) and will end at 10am PST (5pm GMT) on May 3. The free version includes both the campaign, and full multiplayer access<br><br>
<br>
Titanfall 2 came to Steam in June 2020. Links to Apex Legends saw player base hit an all time peak of 16,958 players last week, but has now surpassed that since start of the free weekend, hitting 18,819 players according to Steamcharts.<br>
Earlier in the year, Apex Legends‘ game director Chad Grenier confirmed that Titanfall lore and content would be informing season nine of Apex Legends, saying: “If you’re a Titanfall fan, hang on for season 9 because there’s going to be some really cool stuff in there”.<br>
Senior writer Ashely Reed also spoke about the connection between the two games during a Twitch Panel hosted by BrownGirlGamerCode.<br>
“Apex is what happens after the war, like what is life like in this place?” Reed explained. “We’re trying to integrate Titanfall into that, because Titanfall’s very much a part of the universe.”

Further linking the two titles, a well known Apex Legends dataminer and leaker recently shared a screenshot of a leaked Titanfall 2 weapon in Apex Legends – the Energy Propelled Grenade or EPG – adding to the official revealed information.<br>

Apex Legends ninth season, Legacy, is slated to launch on May 4. The update adds the aforementioned champion Valkyrie , a new 3v3 competitive mode called Arena, and a host of other changes, including sweeping changes to existing champion Lifeline.<br>






