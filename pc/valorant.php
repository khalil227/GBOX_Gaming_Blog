<div class="ArticleTitle1">When will Breeze, VALORANT’s new tropical map, be released?

</div>
  <div class="ArtclTit1">Breeze is the latest map coming to VALORANT. 

  </div>
<img src="../photo/valorant.webp" class="img1A">
<div class="ArticleCon"><br>
    Breeze is a new map coming to VALORANT, taking players to a tropical setting for the first time. <br>

    Players can expect long-range engagements, wide-open spaces, and a colorful environment at the holiday destination. The development team behind the FPS explained some of its objectives with the new map.<br><br>

    “One of the main goals we had with Breeze was building larger and more open spaces with longer sightlines than the other maps in the pool,” said Sal Garozzo, senior game designer at Riot Games. “This provides an opportunity for different weapons and agents to shine. Each map should provide different strengths and weaknesses for the agents in the roster to explore while supporting different types of strategies and team compositions.”<br><br>
<br>
The new map is set for release on April 27. This will also be the first day of Episode Two, Act Three, which means fans can grind the new battle pass while enjoying the Breeze. For players raring to climb the ranks on the beach, it’s unlikely Breeze will be available in competitive matchmaking for the first two weeks of the act.<br>
Players can expect a fresh experience after the cold and brutal environment featured in Icebox. VALORANT developer Devon Fay confirmed Breeze was inspired by islands in the Caribbean and that the team was excited for the “open, bright, colorful world of Breeze.”<br>

The new map will likely force players to adapt their playstyle for long-range engagements. This might also create a new meta for team compositions as squads adjust to the new style, potentially giving a few of VALORANT’s more maligned agents some time in the sun.<br>






