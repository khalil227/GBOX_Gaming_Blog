    Far Cry 6's DLC will let us experience the game as some of the franchise's most infamous villains. <br>

A prematurely-released video – now since-deleted – intimates that upcoming DLC will permit players to play as Far Cry 3's Vaas Montenegro, 4's Pagan Min, and Far Cry 5's charismatic cult leader, Joseph Seed. <br>

The news comes via a leak that's sprung ahead of today's Ubisoft Forward presentation. Despite attempts to smother the leak before the show, it shouldn't take anyone long to track down one of the plentiful copies of the trailer that excited fans managed to grab before it was removed.<br>

We may find out more at E3 2021's Ubisoft Forward – formally, this time – when it kicks off later today at 12am PT / 8pm BST.<br>

Far Cry 6's release date has been confirmed for October 7, 2021, for Amazon Luna, PC, PS5, PS4, Google Stadia, Xbox Series X, Xbox Series S, and Xbox One, and this time around, Ubisoft wants us to explore the world of Far Cry without a gun in our hand.<br>

ICYMI, we recently learned that the missions in Far Cry 6 can be played in almost any order and that taking on Giancarlo Esposito's character, Antón Castillo, may require story progression first. When players do reach Anton, Esposito has confirmed that there will be multiple endings to unlock and that once we've finished with the main campaign, there will be "something else" for players to do, too. <br>

As for the gameplay itself? As we summarized yesterday, so far, there looks to be plenty to do across Yara, including weapon crafting, vehicle modification, gear combinations, crocodiles, and more, although we now know Ubisoft has scrapped a map editor so that the studio can focus on developing other features.<br>

For more, check out our Far Cry 6 preview, where Rachel reveals that there's not just "more chaos, more weapons, and more gear, but a whole country to play with"... "and yes, awesome animal companions are back". <br>

It's not the first Ubisoft leak to escape ahead of the official show, either. Just yesterday, a Mario + Rabbids sequel – Sparks of Hope – popped up online, too, with Reddit user PracticalBrush12 "predicting" (for which he claims are legal reasons) that the game will be announced this weekend. <br>

