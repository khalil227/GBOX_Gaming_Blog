    <div class="ArticleCon2">
    
    A new third-party PS5 controller has gone on sale for almost $300 - almost as much as entire game consoles.<br>
    
    The PS5 controller has had a bit of a makeover from HexGaming (via TheGamer), but it'll set you back $289. But why so expensive? Well, the controller comes with a bunch of fancy features you don't get on the official DualSense, likely because it's targeting the esports audience.<br>
    
    Firstly, the Hex Rival custom PS5 controller comes in 18 colors, some solid, some patterned, and some with accents. It has 6-in-1 interchangeable thumbsticks that can be set to 3 different heights and 2 different ergonomic thumb grip designs: Domed or concave. The description claims that you can switch the thumbsticks in seconds which would help for those wanting to shift tactics in between rounds or maybe even during a respawn.<br>
    
    Another nifty feature is the ability to remap buttons during a game, with a maximum of 15-buttons being available to remap. There are also rear buttons surrounding the remapping panel which has an indicator for remapping confirmations.<br>


    </div>



