<div class="ArticleCon22">The adaptive triggers are also adjusted with the original seeing its trigger activated at 7mm, but the HexGaming PS5 controller activating at only 2mm. The bumpers have also been reduced in the same way for quicker activation from 1.2mm to 0.5mm. Basically, you'll definitely find the triggers to be more responsive.<br>

    For the cost, this PS5 controller does not include a cable or any additional accessories outside of the 6-in-1 interchangeable thumbsticks, user manual, and an after-sales service card. Despite that, the back does look comfy for your hands and certainly seems like a quality controller for those who like having their quality in the competitive scene.<br>
    
    The PS5 controller isn't the most expensive controller we've seen this week. Turtle Beach announced its upcoming flight yoke stick controller for Xbox and PC which comes in at $349.95. While that's a dedicated piece of kit aimed at hardcore simmers, however, this modified controller is available at a price not far below that of the RRP of the Xbox Series S and the Nintendo Switch.<br>
    
    Of course, if Sony plans to follow through with one of its patents, we could find ourselves playing games on bananas somewhere down the line.<br>
    
