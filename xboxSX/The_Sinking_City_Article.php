    <div class="article">
        <p class="overs">   <span class="span">Frogwares</span> has released an enhanced Xbox Series X/S version of its Lovecraft-inspired open-world detective adventure <span class="span">The Sinking City</span>, although ongoing legal tussles mean there's no free upgrade for existing owners on Xbox One.
        </p>

        <table class="heads">
            <td>
                <p>   <span class="span">The Sinking City</span>, if it's so far managed to pass you by, is set in the 1920s and tells the story of Charles Reed, a private investigator plagued by hellish visions.</p>
                <p> Reed travels to the supernaturally flooded city of Oakmont, Massachusetts, in order to escape "the creeping insanity that afflicts him" - but, would you believe it, cosmic horrors ensue.</p>
            </td>
            <td><img class="case" src="../photo/Sinking1.svg" alt="Game case"></td>
        </table>
        <p class="overs">   Much like Frogwares' well-received Sherlock Holmes titles, <span>The Sinking City</span> is primarily focussed on exploration and investigation, with the game unfolding as a series of open-ended cases requiring deductive smarts (and a smattering of gunplay) to solve.
        </p>

        <img class="cine" src="../photo/Sinking2.svg" alt="Cinematic shot ingame">

        <table class="heads">
            <td>
                <p>   <span class="span">Frogwares</span>' enhanced Xbox Series X/S version - which is a digital-only offering - promises 4K visuals at 60fps, improved loading times, and increased graphical fidelity.</p> 
                <p>Additional quests are also available via the accompanying Merciful Madness DLC, which is included as part of the £64.99 Deluxe Edition. The standard version costs £49.99.</p>
            </td>

            <td><img src="../photo/Sinking3.svg" alt="Complete horror all throughout"></td>
        </table>
        <p class="overs">Unfortunately, as was the case with the similarly specced <span class="span">PlayStation 5</span> version that released earlier this year, Frogwares - the developer and publisher of these latest console editions - is unable to offer free platform upgrades for existing owners on <span>Xbox One</span> due to its ongoing legal tussles with Nacon, publisher of the game's last-gen versions.</p>
